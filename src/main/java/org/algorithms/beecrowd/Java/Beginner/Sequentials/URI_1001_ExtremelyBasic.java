package org.algorithms.beecrowd.Beginner.Sequentials;
import java.io.IOException;
import java.util.*;
public class URI_1001_ExtremelyBasic {
    public static void main(String[] args) throws IOException {

        Scanner sc = new Scanner(System.in);

        //final variables is named uppercase
        final int A = sc.nextInt(); //final = No more changes
        final int B = sc.nextInt();
        final int RESULT = A + B; // variable A plus variable B

        System.out.println("X = " + RESULT); // print the result
    }
}
